# Operador de Asignacion -> =
num = 1 
num2 = 2 

resta = num - num2 # Operador de resta
suma = num + num2 # Operador de suma
multi = num * num2 # Operador de multiplicacion
divi = num / num2 # Operador de division
expo = num ** num2 # Operador de exponenciacion
resto = num % num2 # Operador de resto

# Impresiones

print("La resta es: {r}".format(r=resta))
print("La suma es: {s}".format(s=suma))
print("La multiplicacion es: {m}".format(m=multi))
print("La division es: {d}".format(d=divi))
print("La exponenciacion es: {e}".format(e=expo))
print("El resto es: {re}".format(re=resto))